package br.net.venson.produto.models;

public class MagicItem {
    private String name;
    private String type;
    private String desc;
    private String rarity;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getDesc() {
        return desc;
    }

    public String getRarity() {
        return rarity;
    }
}
